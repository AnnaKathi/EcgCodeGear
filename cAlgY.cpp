bool cAlgY::Authenticate(TMemo* mInfo)
	{
	NonFid->findFeatures(mInfo);
	MemoPrint(mInfo, "(AlgY) authenticate OK"); //Test Klassenkonzept
	return true;
	}
//---------------------------------------------------------------------------
void cAlgY::setBar(TProgressBar* pb)
	{
	ProgressBarSet(pb);
	}
//---------------------------------------------------------------------------
bool cAlgY::TestPB()
	{
	ProgressBegin(5000);

	for (int i = 0; i < 5000; i++)
		ProgressTick();

	ProgressEnd();
	return true;
	}
